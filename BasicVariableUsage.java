// this is a program demonstrating the basic usage of the different variable types in Java

public class BasicVariableUsage {
  public static void main(String[] args) {
   String name;
   int age;
   char bloodgroup;
   
   name = "Aryan Zihan Khan";
   age = 18;
   bloodgroup = 'O';
   
   System.out.println("Name : "+name);
   System.out.println("Age : "+age+" years");
   System.out.print("Blood group : "+bloodgroup+" positive");
}
}