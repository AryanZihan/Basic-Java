//this is a Java program which demonstrates the basic function of do while loop

public class BasicDoWhileLoop {
  public static void main(String[] args) {
    int i;
    i = 1;
      
    do
    {
      System.out.println("Test");
      //the following text will be printed 10 times
      i++;
    }
    while(i<=10);
  }
}  
